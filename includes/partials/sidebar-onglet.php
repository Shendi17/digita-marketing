<script>
document.querySelectorAll('.sidebar-onglet-popup-content').forEach(function(popup) {
  var expand = popup.querySelector('.sidebar-onglet-expand-arrow');
  var closeBtn = popup.querySelector('.sidebar-onglet-close');

  // Ajoute le bouton de fermeture si absent
  if (!closeBtn) {
    closeBtn = document.createElement('span');
    closeBtn.className = 'sidebar-onglet-close';
    closeBtn.title = 'Fermer';
    closeBtn.innerHTML = '&times;';
    popup.appendChild(closeBtn);
  }

  // Plein écran : ajoute la classe CSS, ne touche pas au display
  expand.addEventListener('click', function(e) {
    e.stopPropagation();
    popup.classList.add('popup-fullscreen');
  });
  // Fermer le fullscreen
  closeBtn.addEventListener('click', function(e) {
    e.stopPropagation();
    popup.classList.remove('popup-fullscreen');
  });
});
</script>

